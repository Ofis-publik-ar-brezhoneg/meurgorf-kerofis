        <h2>Langue bretonne, une longue tradition littéraire</h2>
        <p>Souvent considérée, à tort, comme une langue exclusivement orale faute d'enseignement organisé, la langue bretonne à la chance de s’appuyer en fait sur une <strong>longue</strong> <strong>tradition lexicographique et littéraire. </strong>&nbsp;Les premières traces écrites du breton remontent au VIIIème siècle ce qui en fait une des premières langues écrite européenne, avant le français. Dès la Renaissance au moment où les premiers dictionnaires naissent en Europe, Jehan Lagadeuc rédige en 1464 le <em>Catholicon</em>, premier dictionnaire trilingue (breton-français-latin); ce premier dictionnaire breton est aussi le premier dictionnaire français. La tradition littéraire de langue bretonne est ininterrompue depuis le XVème siècle.</p>
